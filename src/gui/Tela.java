    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        int index = cboxPais.getSelectedIndex();
        Cliente c = new Cliente();
        c.setNome(tfNome.getText());
        c.setIdade(Integer.parseInt(tfIdade.getText()));
        c.setTelefone(tfTelefone.getText());
        c.setCredito(Double.parseDouble(tfCredito.getText()));
        if (index == -1) {
            c.setPais(null);
        }
        else {
            c.setPais(ListPais.get(index));
        }
        int x = 0;
        for (int salv = 0; salv < ListCli.size();salv++) {
            if (c.getNome().equals(ListCli.get(salv).getNome())) {
                ListCli.set(salv, c);
            }
        }
        CarregaTable();
    }//GEN-LAST:event_btAlterarActionPerformed

