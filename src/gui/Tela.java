    private void btSalvarPaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarPaisActionPerformed
        Pais p = new Pais();
        p.setNome(tfNomePais.getText());
        p.setSigla(tfSigla.getText());
        int x = 0;
        for (int salv = 0; salv < ListPais.size();salv++) {
            if (p.getNome().equals(ListPais.get(salv).getNome())) {
                x = 1;
            }
        }
        if (x == 1) {
            JOptionPane.showMessageDialog(null, "Nome Existente");
        }
        else{
            ListPais.add(p);
        }
        CarregaTablePais();
        
    }//GEN-LAST:event_btSalvarPaisActionPerformed

