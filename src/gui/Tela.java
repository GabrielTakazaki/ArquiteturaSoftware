    public void CarregaTable () {
        Object colunas[] = {"Nome", "Idade", "Telefone", "Credito","Pais"};
        DefaultTableModel modeloCli = new DefaultTableModel(colunas,0);
        Object linha[]; 
        for (int i = 0; i < ListCli.size(); i++) {
            if (ListCli.get(i).getPais() == null) {
                linha = new Object[] {ListCli.get(i).getNome(),
                            ListCli.get(i).getIdade(), ListCli.get(i).getTelefone(),
                            ListCli.get(i).getCredito()};
            }
            else {
                linha = new Object[] {ListCli.get(i).getNome(),
                            ListCli.get(i).getIdade(), ListCli.get(i).getTelefone(),
                            ListCli.get(i).getCredito(),
                            ListCli.get(i).getPais().getSigla()};
            }
            modeloCli.addRow(linha);
        
        }
        CarregaCBPais();
        tbCliente.setModel(modeloCli);
    }
    
    public void CarregaTablePais () {
        Object colunas[] = {"Nome", "Sigla"};
        DefaultTableModel modeloPais = new DefaultTableModel(colunas,0);
        
        for (int i = 0; i < ListPais.size(); i++) {
            Object linha[] = new Object[] {ListPais.get(i).getNome(),
                            ListPais.get(i).getSigla()};
            modeloPais.addRow(linha);
        
        }
        CarregaCBPais();
        tbPais.setModel(modeloPais);
    }
    public void CarregaCBPais () {
        cboxPais.addItem(" ");
        for (int i = 0; i<ListPais.size();i++) {
            cboxPais.addItem(ListPais.get(i).getSigla());
        }
    }

