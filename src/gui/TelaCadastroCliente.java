    private void popularComboBox() {
        paisDAO = new PaisDAO(paises);
        
        if (paisDAO.listarPaises() != null){
            for (Pais pais : paisDAO.listarPaises()){
                cboxPais.addItem(pais.getNome());
            }
        }
    }
